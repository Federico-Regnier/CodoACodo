    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        Integer selectedRow = tablaListados.getSelectedRow();
        if(selectedRow < 0)
            return;
        
        txtIdModificar.setText(tablaListados.getValueAt(selectedRow, 0).toString());
        txtNombreCompletoModificar.setText(tablaListados.getValueAt(selectedRow, 1).toString());
        txtUserModificar.setText(tablaListados.getValueAt(selectedRow, 2).toString());
        Integer nivel = Integer.parseInt(tablaListados.getValueAt(selectedRow, 3).toString());
        cmbNivelAccesoModificar.setSelectedItem(nivelAcceso.from(nivel));
        
        panelMenuAdministrador.setVisible(false);
        panelModificarUsuario.setVisible(true);
        
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        Integer selectedRow = tablaListados.getSelectedRow();
        if(selectedRow <= 0)
            return;
        
        Integer id = Integer.parseInt(tablaListados.getValueAt(selectedRow, 0).toString());
        
        if(JOptionPane.showConfirmDialog(this, "Desea eliminar el usuario?", "Eliminar Usuario", JOptionPane.YES_NO_OPTION) == JOptionPane.YES_NO_OPTION){
            Biblioteca.connect();
            if(Biblioteca.borrarUsuario(id)){
                ((DefaultTableModel)tablaListados.getModel()).removeRow(selectedRow);
            } else{
                JOptionPane.showMessageDialog(this, "Error al Eliminar el usuario", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnAceptarModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarModificarActionPerformed
        Integer id = Integer.parseInt(txtIdModificar.getText());
        String user = txtUserModificar.getText();
        String nombreCompleto = txtNombreCompletoModificar.getText();
        String password = new String(txtPasswordModificar.getPassword());
        Integer nivelAcceso = ((nivelAcceso)cmbNivelAccesoModificar.getSelectedItem()).getNivel();
        Usuario usuario = new Usuario(id, nombreCompleto, user, nivelAcceso);
        
        Biblioteca.connect();
        
        if(!password.equals("")){
            Biblioteca.cambiarPass(id, password);
        }
        
        if(Biblioteca.modificarUsuario(usuario)){
            JOptionPane.showMessageDialog(this, "Usuario modificado con exito", "Exito", JOptionPane.INFORMATION_MESSAGE);
            panelModificarUsuario.setVisible(false);
            panelMenuAdministrador.setVisible(true);
            UpdateTablaUsuarios();
        } else {
            JOptionPane.showMessageDialog(this, "No se pudo modificar el usuario.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnAceptarModificarActionPerformed

