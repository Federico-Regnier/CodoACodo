    private void UpdateTablaUsuarios(){
        tablaListados.setDefaultEditor(Object.class, null);
        DefaultTableModel model = (DefaultTableModel) tablaListados.getModel();
        model.setColumnCount(0);
        model.setRowCount(0);
        model.addColumn("ID");
        model.addColumn("Nombre Completo");
        model.addColumn("Usuario");
        model.addColumn("Nivel de Acceso");
        
        Biblioteca.connect();
        List<Usuario> usuarios = Biblioteca.getUsers();
        if(usuarios != null){
            for(Usuario user : usuarios){
                String[] fila = new String[4];
                fila[0]  = user.getId().toString();
                fila[1] = user.getNombreCompleto();
                fila[2] = user.getUsuario();
                fila[3] = user.getNivelAcceso().toString();
                
                model.addRow(fila);
            }
        }
    }
    
